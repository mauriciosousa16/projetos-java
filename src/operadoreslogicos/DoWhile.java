package operadoreslogicos;

public class DoWhile {
	public static void main(String[] args) {
		
		
		// excuta primeiro deppis verifica
		
		int numero = 0;
		
		do {
			
			System.out.println(" o numero ataual e : " + numero);
			numero++;
			
			
		} while (numero <= 18);
	}

}
